    ShareSocial
  },
  computed: {
    ...mapState(["essays"]),
    articleTag() {
      let tags = [];
      let articleTitle = this.$route.params.essayTitle;
      for (let i = 0; i < this.essays.length; i++) {
        if (this.essays[i].essayTitle === articleTitle) {
          tags = this.essays[i].tags;
          break;
        }
      }
      return tags;
    }
  }
